# Rules for naming the Variables #

#1. Variable name must begin with an alphabet or an underscore(_)

#abc = 100
#_abc = 100
# 3a = 10
# @abc = 100

#2. The first character can be followed by alphabets,  numbers or underscore

# a100 = 100
# _a984_ = 100
# # a996$ = 100
# xyz- = 2


#3. Variable names are case sensitive

# a100 = 100
# A100 = 200
# print("a100: ", a100, "\nA100: ", A100)

#4. Reserved words cannot be used as variable names

# break = 10
# class = 5
# try = 100

